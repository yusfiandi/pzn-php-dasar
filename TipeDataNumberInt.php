<?php
// Tipe Data - Integer
echo "Decimal :";
var_dump(1234);

echo "Octal :";
var_dump(01234);

echo "Hexadecimal:";
var_dump(0x1A);

echo "Binary :";
var_dump(0b11011);

echo "Underscore in Number :";
var_dump(200_000);

// Tipe Data - Float
echo "Floating Point :";
var_dump(1.234);

echo "Floating Point dengan E notation : (1.7 * 1000) :";
var_dump(1.7e3);

echo "Floating Point dengan E notation minus : (1.7 * 0.001) :";
var_dump(1.7e-3);

echo "Underscore Floating Point :";
var_dump(1_234.123);
?>